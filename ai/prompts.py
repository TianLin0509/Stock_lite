def _build_price_snapshot(df: pd.DataFrame) -> str:
    """从K线DataFrame构建价格快照摘要（供非趋势分析模块使用）"""
    if df is None or isinstance(df, str) or (isinstance(df, pd.DataFrame) and df.empty):
        return ""
    try:
        d = df.copy()
        close = d["收盘"].astype(float)
        current = close.iloc[-1]

        def _pct(n):
            if len(d) <= n:
                return "N/A"
            return f"{(close.iloc[-1] / close.iloc[-n] - 1) * 100:+.1f}%"

        high_60 = d.tail(60)["最高"].astype(float).max() if len(d) >= 60 else d["最高"].astype(float).max()
        low_60 = d.tail(60)["最低"].astype(float).min() if len(d) >= 60 else d["最低"].astype(float).min()
        range_60 = high_60 - low_60
        position = f"{(current - low_60) / range_60 * 100:.0f}%" if range_60 > 0 else "N/A"

        return (
            f"\n## 近期走势快照\n"
            f"最新收盘: {current:.2f}元 | 5日: {_pct(5)} | 20日: {_pct(20)} | 60日: {_pct(60)}\n"
            f"60日区间: {low_60:.2f}~{high_60:.2f} | 当前位置: {position}（0%=最低, 100%=最高）"
        )
    except Exception:
        return ""


def _build_fin_snapshot(financial: str) -> str:
    """从财务数据文本提取关键增长指标"""
    if not financial:
        return ""
    # 财务数据已经是文本格式，直接截取核心部分
    return financial[:1800] if financial else ""


